/**
 * Generate Excel workbook
 */
export async function exportToExcel(
  data: ExportData,
  type: ExportType,
  options: ExportOptions = {}
): Promise<Buffer> {
  const locale = options.locale || "fr";
  const currency = options.currency || config.app.currency;
  const t = translations[locale];

  const workbook = XLSX.utils.book_new();

  switch (type) {
    case "debts":
      addDebtsSheet(workbook, data.debts || [], t, locale, currency, options.isDemo);
      break;
    case "clients":
      addClientsSheet(workbook, data.clients || [], t, locale, options.isDemo);
      break;
    case "reminders":
      addRemindersSheet(workbook, data.reminders || [], t, locale, options.isDemo);
      break;
  }

  // Generate buffer
  const excelBuffer = XLSX.write(workbook, { type: "buffer", bookType: "xlsx" });
  return Buffer.from(excelBuffer);
}

/**
 * Add debts sheet to workbook
 */
function addDebtsSheet(
  workbook: XLSX.WorkBook,
  debts: Array<Debt & { client?: { name: string; email?: string | null; phone?: string | null; company?: string | null } }>,
  t: typeof translations.fr,
  locale: Locale,
  currency: string,
  isDemo?: boolean
): void {
  const headers = [
    t.debts.reference,
    t.debts.client,
    t.debts.amount,
    t.debts.paid,
    t.debts.remaining,
    t.debts.dueDate,
    t.debts.status,
    t.debts.description,
  ];

  const rows = debts.map((debt) => [
    debt.reference || "",
    debt.client?.name || "",
    debt.amount,
    debt.paidAmount,
    debt.amount - debt.paidAmount,
    formatDate(debt.dueDate, locale),
    getStatusLabel("debt", debt.status, locale),
    debt.description || "",
  ]);

  // Add totals row
  const totalAmount = debts.reduce((sum, d) => sum + d.amount, 0);
  const totalPaid = debts.reduce((sum, d) => sum + d.paidAmount, 0);
  rows.push([
    t.total,
    `${debts.length} ${t.records}`,
    totalAmount,
    totalPaid,
    totalAmount - totalPaid,
    "",
    "",
    "",
  ]);

  const worksheet = XLSX.utils.aoa_to_sheet([headers, ...rows]);

  // Set column widths
  worksheet["!cols"] = [
    { wch: 15 }, // Reference
    { wch: 25 }, // Client
    { wch: 15 }, // Amount
    { wch: 15 }, // Paid
    { wch: 15 }, // Remaining
    { wch: 12 }, // Due Date
    { wch: 12 }, // Status
    { wch: 30 }, // Description
  ];

  // Add currency formatting info
  const currencyInfo = `Devise: ${getCurrencySymbol(currency)} (${currency})`;
  XLSX.utils.sheet_add_aoa(worksheet, [[currencyInfo]], { origin: -1 });

  // Add demo watermark if in demo mode
  if (isDemo) {
    XLSX.utils.sheet_add_aoa(worksheet, [
      [""],
      ["⚠️ Document généré en mode démonstration - RelancePro Africa"],
      ["Ce document contient des données de démonstration et ne doit pas être utilisé à des fins professionnelles."],
    ], { origin: -1 });
  }

  XLSX.utils.book_append_sheet(workbook, worksheet, t.debts.title.substring(0, 31));
}

