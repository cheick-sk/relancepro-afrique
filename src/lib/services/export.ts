/**
 * Format a number with space separators (e.g., "50 000")
 */
function formatNumberWithSpaces(num: number): string {
  return num.toString().replace(/\B(?=(\d{3})+(?!\d))/g, " ");
}

/**
 * Format currency amount for display
 */
function formatCurrency(amount: number, currency: string = "GNF"): string {
  const symbol = getCurrencySymbol(currency);
  return `${formatNumberWithSpaces(Math.round(amount))} ${symbol}`;
}

/**
 * Format date for display
 */
function formatDate(date: Date | string, locale: Locale = "fr"): string {
  const d = typeof date === "string" ? new Date(date) : date;
  return d.toLocaleDateString(locale === "fr" ? "fr-FR" : "en-US", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  });
}

/**
 * Get status label based on type and status value
 */
function getStatusLabel(
  type: "debt" | "client" | "reminder",
  status: string,
  locale: Locale
): string {
  const t = translations[locale];
  const statusMap: Record<string, string> =
    type === "debt"
      ? t.debts.statuses
      : type === "client"
        ? t.clients.statuses
        : t.reminders.statuses;
  return statusMap[status] || status;
}

