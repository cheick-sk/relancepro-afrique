/**
 * Generate PDF document
 */
export async function exportToPDF(
  data: ExportData,
  type: ExportType,
  options: ExportOptions = {}
): Promise<Buffer> {
  const locale = options.locale || "fr";
  const currency = options.currency || config.app.currency;
  const t = translations[locale];

  // Create PDF document
  const doc = new jsPDF({
    orientation: type === "reminders" ? "landscape" : "portrait",
    unit: "mm",
    format: "a4",
  });

  // Colors
  const primaryColor: [number, number, number] = [41, 128, 185]; // Blue
  const textColor: [number, number, number] = [44, 62, 80]; // Dark gray
  const lightGray: [number, number, number] = [189, 195, 199];

  // ===== HEADER =====
  // Logo/Brand area
  doc.setFillColor(...primaryColor);
  doc.rect(0, 0, 210, 35, "F");

  // Company name
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(22);
  doc.setFont("helvetica", "bold");
  doc.text(t.appName, 14, 18);

  // Tagline
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");
  doc.text(t.tagline, 14, 26);

  // Export date (right side)
  doc.setFontSize(9);
  const exportDateStr = `${t.exportDate}: ${formatDate(new Date(), locale)}`;
  const textWidth = doc.getTextWidth(exportDateStr);
  doc.text(exportDateStr, 196 - textWidth, 18);

  // Company name if provided
  if (options.companyName) {
    doc.text(options.companyName, 196 - doc.getTextWidth(options.companyName), 26);
  }

  // ===== TITLE =====
  doc.setTextColor(...textColor);
  doc.setFontSize(16);
  doc.setFont("helvetica", "bold");
  
  const title = type === "debts" ? t.debts.title : type === "clients" ? t.clients.title : t.reminders.title;
  doc.text(title, 14, 48);

  // Date range if provided
  if (options.dateRange) {
    doc.setFontSize(10);
    doc.setFont("helvetica", "normal");
    const rangeText = `${formatDate(options.dateRange.start, locale)} - ${formatDate(options.dateRange.end, locale)}`;
    doc.text(rangeText, 14, 56);
  }

  // ===== DATA TABLE =====
  let startY = options.dateRange ? 62 : 54;

  switch (type) {
    case "debts":
      generateDebtsPDFTable(doc, data.debts || [], startY, t, locale, currency, textColor);
      break;
    case "clients":
      generateClientsPDFTable(doc, data.clients || [], startY, t, locale, textColor);
      break;
    case "reminders":
      generateRemindersPDFTable(doc, data.reminders || [], startY, t, locale, textColor);
      break;
  }

  // ===== FOOTER =====
  const pageCount = doc.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    
    // Add demo watermark if in demo mode
    if (options.isDemo) {
      // Save current state
      doc.saveGraphicsState?.();
      
      // Add watermark text at center of page (rotated)
      const pageWidth = doc.internal.pageSize.getWidth();
      const pageHeight = doc.internal.pageSize.getHeight();
      
      doc.setTextColor(220, 220, 220);
      doc.setFontSize(40);
      doc.setFont("helvetica", "bold");
      
      // Draw diagonal watermark
      doc.text("MODE DÉMO", pageWidth / 2, pageHeight / 2, {
        angle: 45,
        align: "center",
      });
      
      // Restore state
      doc.restoreGraphicsState?.();
      
      // Add demo notice at bottom of page
      doc.setTextColor(255, 150, 0);
      doc.setFontSize(10);
      doc.setFont("helvetica", "bold");
      const demoNotice = "⚠️ Document généré en mode démonstration - RelancePro Africa";
      doc.text(demoNotice, 14, 274);
    }
    
    // Footer line
    doc.setDrawColor(...lightGray);
    doc.line(14, 280, 196, 280);

    // Footer text
    doc.setFontSize(8);
    doc.setTextColor(...textColor);
    doc.setFont("helvetica", "normal");
    doc.text(t.footer, 14, 286);

    // Page number
    const pageText = `${t.page} ${i} ${t.of} ${pageCount}`;
    doc.text(pageText, 196 - doc.getTextWidth(pageText), 286);
  }

  // Return as Buffer
  const pdfOutput = doc.output("arraybuffer");
  return Buffer.from(pdfOutput);
}

/**
 * Generate debts table for PDF
 */
function generateDebtsPDFTable(
  doc: jsPDF,
  debts: Array<Debt & { client?: { name: string; email?: string | null; phone?: string | null; company?: string | null } }>,
  startY: number,
  t: typeof translations.fr,
  locale: Locale,
  currency: string,
  textColor: [number, number, number]
): void {
  const tableData = debts.map((debt) => [
    debt.reference || "-",
    debt.client?.name || "-",
    formatCurrency(debt.amount, debt.currency || currency),
    formatCurrency(debt.paidAmount, debt.currency || currency),
    formatCurrency(debt.amount - debt.paidAmount, debt.currency || currency),
    formatDate(debt.dueDate, locale),
    getStatusLabel("debt", debt.status, locale),
  ]);

  // Calculate totals
  const totalAmount = debts.reduce((sum, d) => sum + d.amount, 0);
  const totalPaid = debts.reduce((sum, d) => sum + d.paidAmount, 0);
  const totalRemaining = totalAmount - totalPaid;

  autoTable(doc, {
    startY,
    head: [
      [
        t.debts.reference,
        t.debts.client,
        t.debts.amount,
        t.debts.paid,
        t.debts.remaining,
        t.debts.dueDate,
        t.debts.status,
      ],
    ],
    body: tableData,
    foot: [
      [
        { content: t.total, styles: { fontStyle: "bold", halign: "left" } },
        { content: `${debts.length} ${t.records}`, styles: { fontStyle: "bold" } },
        { content: formatCurrency(totalAmount, currency), styles: { fontStyle: "bold" } },
        { content: formatCurrency(totalPaid, currency), styles: { fontStyle: "bold" } },
        { content: formatCurrency(totalRemaining, currency), styles: { fontStyle: "bold" } },
        "",
        "",
      ],
    ],
    theme: "grid",
    headStyles: {
      fillColor: [41, 128, 185],
      textColor: [255, 255, 255],
      fontStyle: "bold",
      fontSize: 9,
      halign: "center",
    },
    bodyStyles: {
      textColor,
      fontSize: 8,
    },
    footStyles: {
      fillColor: [236, 240, 241],
      textColor,
      fontSize: 8,
    },
    columnStyles: {
      0: { cellWidth: 25 },
      1: { cellWidth: 35 },
      2: { cellWidth: 30, halign: "right" },
      3: { cellWidth: 25, halign: "right" },
      4: { cellWidth: 30, halign: "right" },
      5: { cellWidth: 25, halign: "center" },
      6: { cellWidth: 20, halign: "center" },
    },
    alternateRowStyles: {
      fillColor: [245, 247, 250],
    },
    margin: { left: 14, right: 14 },
  });
}

