/**
 * Export all data to a single Excel workbook with multiple sheets
 */
export async function exportAllToExcel(
  data: ExportData,
  options: ExportOptions = {}
): Promise<Buffer> {
  const locale = options.locale || "fr";
  const currency = options.currency || config.app.currency;
  const t = translations[locale];

  const workbook = XLSX.utils.book_new();

  // Add all sheets
  if (data.debts && data.debts.length > 0) {
    addDebtsSheet(workbook, data.debts, t, locale, currency);
  }

  if (data.clients && data.clients.length > 0) {
    addClientsSheet(workbook, data.clients, t, locale);
  }

  if (data.reminders && data.reminders.length > 0) {
    addRemindersSheet(workbook, data.reminders, t, locale);
  }

  // Generate buffer
  const excelBuffer = XLSX.write(workbook, { type: "buffer", bookType: "xlsx" });
  return Buffer.from(excelBuffer);
}

